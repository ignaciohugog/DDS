package app.vista.notificaciones;

import org.uqbar.arena.windows.WindowOwner;

public class BusquedaFallidaPorFechaConFormatoIncorrecto<T> extends BusquedaFallida<T>
{

	public BusquedaFallidaPorFechaConFormatoIncorrecto(WindowOwner owner, T model)
	{
		super(owner, model);
		this.setMensaje("Por favor verifique que la fecha tenga un " +
				"formato dd/MM/aaaa");
	}

}
