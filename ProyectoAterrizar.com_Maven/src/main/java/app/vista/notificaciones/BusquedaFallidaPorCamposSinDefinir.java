package app.vista.notificaciones;

import org.uqbar.arena.windows.WindowOwner;

public class BusquedaFallidaPorCamposSinDefinir<T> extends BusquedaFallida<T>
{

	public BusquedaFallidaPorCamposSinDefinir(WindowOwner owner, T model)
	{
		super(owner, model);
		this.setMensaje("Por favor complete los campos de origen, " +
				"destino y fecha de salida");	
	}
}
